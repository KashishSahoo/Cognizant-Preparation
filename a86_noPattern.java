public class a86_noPattern {
    public static void main(String[] args) {
        int r=5;
        for (int i = 1; i <=r; i++) {
            int n=1;
            for(int j=1;j<=i;j++){
                System.out.print(n);
                n++;
            }
            System.out.println();
        }
    }
}
